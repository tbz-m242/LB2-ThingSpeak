int main()
{
    uint8_t id;
    float value1, value2, h = 0.0f, c = 0.0f;
    int rc = 0;
    
    printf("\tLB02RedunantTemp\n");

    /* Init all sensors with default params */
    hum_temp.init(NULL);
    hum_temp.enable();
    
    hum_temp.read_id(&id);
    printf("HTS221  humidity & temperature    = 0x%X\r\n", id);    
    
    // Connect to the network with the default networking interface
    // if you use WiFi: see mbed_app.json for the credentials
    NetworkInterface* network = connect_to_default_network_interface();
    
    if (!network) {
        printf("Cannot connect to the network, see serial output\n");
        return 1;
    }

    while( 1 ) 
    {
        hum_temp.get_temperature(&value1);
        hum_temp.get_humidity(&value2);
        
        rc = sensor.readData();
        if ( rc == 0 )
        {
            c   = sensor.ReadTemperature(CELCIUS);
            h   = sensor.ReadHumidity();
        }
        else
            return  ( -1 );         // Fehler - Programm beenden
    
        
        sprintf( message, "%s?key=%s&field1=%f&field2=%f&field3=%f&field4=%f", host, key, value1, value2, c, h );
        printf( "%s\n", message );
        oled.cursor( 1, 0 );
        oled.printf( "temp: %3.2f   %4.2f\nhum : %3.2f   %4.2f", value1, c, value2, h );
        
        myled = 1;
        HttpsRequest* get_req = new HttpsRequest( network, SSL_CA_PEM, HTTP_GET, message );

        HttpResponse* get_res = get_req->send();
        if (!get_res) 
        {
            printf("HttpsRequest failed (error code %d)\n", get_req->get_error());
            return 1;
        }
        delete get_req;     
        myled = 0;   
        wait(10);
    }
}
